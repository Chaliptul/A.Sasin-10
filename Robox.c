#include <stdio.h>

int main() {
    int n;                  // จำนวนครั้งที่เคลื่อนที่
    int i;                  // ตัวนับลูป
    int move;               // ตัวเก็บค่าทิศทางที่ผู้ใช้ป้อน
    int x = 0, y = 0;       // ตำแหน่งของหุ่นยนต์ (เริ่มที่ 0,0)
    int count[9] = {0};     // เก็บจำนวนครั้งของแต่ละทิศ (1–8)

    

    printf("จำนวนครั้งที่หุ่นยนต์จะเคลื่อนที่: ");
    scanf("%d", &n);

    printf("ป้อนเส้นทาง (1–8):\n");
    printf("เส้นทางของหุ่นยนต์ กรอกเลขตามตำแหน่งที่อยากให้หุ่นยนต์ไปในแต่ละครั้ง 1:บน, 2:ล่าง, 3:ซ้าย, 4:ขวา  5:ซ้ายบน, 6:ขวาบน, 7:ซ้ายล่าง, 8:ขวาล่าง\n");

    for (i = 0; i < n; i++) {
        printf("ครั้งที่ %d: ", i + 1);
        scanf("%d", &move);

        if (move < 1 || move > 8) {
            printf("กรุณาใส่เลขระหว่าง 1–8 เท่านั้น!\n");
            return 0;
        }

        // นับจำนวนแต่ละทิศ
        count[move]++;

        // เคลื่อนที่หุ่นยนต์ตามทิศ
        switch (move) {
            case 1: y++; break;           // ขึ้น
            case 2: y--; break;           // ลง
            case 3: x--; break;           // ซ้าย
            case 4: x++; break;           // ขวา
            case 5: x--; y++; break;      // ซ้ายบน
            case 6: x++; y++; break;      // ขวาบน
            case 7: x--; y--; break;      // ซ้ายล่าง
            case 8: x++; y--; break;      // ขวาล่าง
        }
    }

    printf("\n==== สรุปผลการเคลื่อนที่ ====\n");
    printf("ตำแหน่งสุดท้ายของหุ่นยนต์: (x=%d, y=%d)\n", x, y);
    printf("สถิติการเคลื่อนที่แต่ละทิศ:\n");
    for (i = 1; i <= 8; i++) {
        printf("ทิศ %d : %d ครั้ง\n", i, count[i]);
    }

    printf("=============================\n");
    return 0;
}
